#!/usr/bin/env bash
#
# scripts/coverage/measure.sh
#
# Runs the Sonar test suite with code coverage enabled and renders a
# Markdown report to docs/coverage.md. Requires xcodebuild, xcrun and jq.
#
# Usage:
#   scripts/coverage/measure.sh
#
set -euo pipefail

# --- Paths -------------------------------------------------------------------
REPO_ROOT="$(cd "$(dirname "${BASH_SOURCE[0]}")/../.." && pwd)"
cd "$REPO_ROOT"

DERIVED_DATA="build/CoverageDerived"
COVERAGE_JSON="/tmp/sonar-coverage.json"
REPORT_PATH="docs/coverage.md"

pick_iphone_simulator() {
  local sims_json
  sims_json="$(mktemp)"
  xcrun simctl list devices available --json >"$sims_json"
  if python3 - "$sims_json" <<'PY'
import json
import os
import sys

preferred = os.environ.get("SIMULATOR_NAME", "iPhone 16 Pro")
with open(sys.argv[1], encoding="utf-8") as handle:
    data = json.load(handle)
iphones = []
for runtime, devices in data.get("devices", {}).items():
    if "iOS" not in runtime:
        continue
    for device in devices:
        if device.get("isAvailable") and "iPhone" in device.get("name", ""):
            iphones.append(device)
if not iphones:
    raise SystemExit("no available iPhone simulator found")
for device in iphones:
    if device.get("name") == preferred:
        print(device["udid"])
        break
else:
    print(iphones[0]["udid"])
PY
  then
    local status=0
  else
    local status=$?
  fi
  rm -f "$sims_json"
  return "$status"
}

SIMULATOR_ID="${SIMULATOR_ID:-${SIM_UDID:-}}"
if [[ -z "$SIMULATOR_ID" ]]; then
  SIMULATOR_ID="$(pick_iphone_simulator)"
fi

mkdir -p "$(dirname "$REPORT_PATH")"

# --- 1. Run tests with coverage ---------------------------------------------
echo "==> Running xcodebuild test with coverage (this can take a few minutes)..."
echo "==> Simulator: ${SIMULATOR_ID}"
xcodebuild test -project Sonar.xcodeproj -scheme Sonar \
  -destination "platform=iOS Simulator,id=${SIMULATOR_ID}" \
  -onlyUsePackageVersionsFromResolvedFile \
  -skipPackageUpdates \
  -scmProvider system \
  -packageAuthorizationProvider netrc \
  -enableCodeCoverage YES \
  -derivedDataPath "$DERIVED_DATA" 2>&1 | tail -10

# --- 2. Extract coverage JSON ------------------------------------------------
XCRESULT_PATH="$(find "$DERIVED_DATA" -name '*.xcresult' | head -1)"
if [[ -z "${XCRESULT_PATH}" ]]; then
  echo "ERROR: no .xcresult bundle found under ${DERIVED_DATA}" >&2
  exit 1
fi

echo "==> Extracting coverage JSON from ${XCRESULT_PATH}"
xcrun xccov view --report --json "$XCRESULT_PATH" > "$COVERAGE_JSON"

# --- 3. Render Markdown report ----------------------------------------------
echo "==> Rendering ${REPORT_PATH}"

GENERATED_AT="$(date -u +"%Y-%m-%dT%H:%M:%SZ")"

# Total line coverage (fraction 0..1) -> percent with 2 decimals.
TOTAL_PCT="$(jq -r '.lineCoverage * 100 | . * 100 | round / 100' "$COVERAGE_JSON")"

# Per-file rows, sorted ascending by coverage (untested first).
# Schema: targets[].files[].{path, coveredLines, executableLines, lineCoverage}
PER_FILE_ROWS="$(jq -r '
  [ .targets[]?.files[]?
    | { path: .path,
        pct: ((.lineCoverage // 0) * 10000 | round / 100),
        covered: (.coveredLines // 0),
        total: (.executableLines // 0) }
  ]
  | sort_by(.pct)
  | .[]
  | "| `\(.path)` | \(.pct)% | \(.covered) / \(.total) |"
' "$COVERAGE_JSON")"

{
  echo "# Sonar Code Coverage"
  echo
  echo "**Generated at:** \`${GENERATED_AT}\`"
  echo
  echo "## Total"
  echo
  echo "**Line coverage:** \`${TOTAL_PCT}%\`"
  echo
  echo "## Per-file (sorted ascending — untested floats to the top)"
  echo
  echo "| File | % covered | Lines covered / total |"
  echo "| --- | ---: | ---: |"
  echo "${PER_FILE_ROWS}"
  echo
  echo "---"
  echo
  echo "Generated by \`scripts/coverage/measure.sh\` — re-run anytime."
} > "$REPORT_PATH"

echo "==> Done. Report written to ${REPORT_PATH}"
